                    <p align="justify"><font size="2" face="Arial, Helvetica, sans-serif"><br>
                      <strong><font color="#003399">FactSage</font></strong>&copy;, 
                      one of the largest fully integrated database computing systems 
                      in chemical thermodynamics in the world, was introduced 
                      in 2001 and is the fusion of the <strong><font color="#003399">FACT-Win</font></strong>/<strong><font color="#003399">F*A*C*T</font></strong> 
                      and <strong><font color="#003399">ChemSage</font></strong>/<strong><font color="#003399">SOLGASMIX</font></strong> 
                      thermochemical packages. <strong><font color="#003399">FactSage</font></strong> 
                      is the result of over 20 years of collaborative efforts 
                      between Thermfact/CRCT (Montreal, Canada) <a href="http://www.crct.polymtl.ca" target="_blank">www.crct.polymtl.ca</a> 
                      and GTT-Technologies (Aachen, Germany) <a href="http://www.gtt-technologies.de" target="_blank">www.gtt-technologies.de</a>.<br>
                      <br>
                      The <strong><font color="#003399">FactSage</font></strong> 
                      package runs on a PC operating under Microsoft Windows&reg; 
                      and consists of a series of information, database, calculation 
                      and manipulation modules that access various pure substances 
                      and solution databases. <strong><font color="#003399">FactSage</font></strong> 
                      has several hundred industrial, governmental and academic 
                      users in materials science, pyrometallurgy, hydrometallurgy, 
                      electrometallurgy, corrosion, glass technology, combustion, 
                      ceramics, geology, etc. It is used internationally in graduate 
                      and undergraduate teaching and research.<br>
                      <br>
                      Users have access to databases of thermodynamic data for 
                      thousands of compounds as well as to evaluated and optimized 
                      databases for hundreds of solutions of metals, liquid and 
                      solid oxide solutions, mattes, molten and solid salt solutions, 
                      aqueous solutions, etc. The<strong><font color="#003399"> 
                      FactSage</font></strong> software automatically accesses 
                      these databases. The evaluated databases for oxides, slags, 
                      mattes, etc. have been developed by optimization of literature 
                      data using advanced modeling techniques, several of which 
                      have been developed at the <a href="http://www.crct.polymtl.ca" target="_blank">CRCT</a>. 
                      <strong><font color="#003399">FactSage</font></strong> can 
                      also access the databases for alloy solutions developed 
                      by the international <a href="http://www.sgte.org" target="_blank">SGTE 
                      Group</a>, and the databases for steels, light metal alloys 
                      and other alloy systems developed by <a href="http://www.spencergroupintl.com/" target="_blank">The 
                      Spencer Group</a>, <a href="http://www.gtt-technologies.de" target="_blank">GTT-Technologies</a> 
                      and the <a href="http://www.crct.polymtl.ca" target="_blank">CRCT</a>. 
                       For detailed information on the various 
                      databases click on <a href="http://www.crct.polymtl.ca/fact/documentation/" target="_blank">Database 
                      Documentation</a>.<br>
                      <br>
                      With <strong><font color="#003399">FactSage</font></strong> 
                      you can calculate the conditions for multiphase, multicomponent 
                      equilibria, with a wide variety of tabular and graphical 
                      output modes, under a large range of constraints. For example, 
                      general N-component phase diagram sections can be easily 
                      generated with a wide choice of axis variables; matte/metal/slag/gas/solid 
                      equilibria can be accurately calculated, tabulated and plotted 
                      for industrial systems; multicomponent predominance and 
                      EpH diagrams can be readily produced; the course of equilibrium 
                      or non-equilibrium solidification can be followed; complex 
                      heat balances can be computed; and so on. <br>
